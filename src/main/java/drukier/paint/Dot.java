package drukier.paint;

public class Dot {
    private int dotX;
    private int dotY;

    public Dot(int dotX, int dotY) {
        this.dotX = dotX;
        this.dotY = dotY;
    }

    public int getDotX() {
        return dotX;
    }

    public void setDotX(int dotX) {
        this.dotX = dotX;
    }

    public int getDotY() {
        return dotY;
    }

    public void setDotY(int dotY) {
        this.dotY = dotY;
    }
}
